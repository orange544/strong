CREATE DATABASE IF NOT EXISTS movie_clickhouse_db;

DROP TABLE IF EXISTS movie_clickhouse_db.boxoffice_fact;
DROP TABLE IF EXISTS movie_clickhouse_db.schedule_attendance_fact;
DROP TABLE IF EXISTS movie_clickhouse_db.city_boxoffice_stat;
DROP TABLE IF EXISTS movie_clickhouse_db.movie_rating_distribution;
DROP TABLE IF EXISTS movie_clickhouse_db.movie_popularity_trend;
DROP TABLE IF EXISTS movie_clickhouse_db.recommend_exposure_click_fact;
DROP TABLE IF EXISTS movie_clickhouse_db.search_heat_stat;
DROP TABLE IF EXISTS movie_clickhouse_db.movie_hot_ranking_snapshot;

CREATE TABLE movie_clickhouse_db.boxoffice_fact (
  stat_date Date,
  movie_id UInt64,
  cinema_id UInt64,
  schedule_id UInt64,
  ticket_order_count UInt64,
  payment_amount Decimal(18,2)
)
ENGINE = Log;

CREATE TABLE movie_clickhouse_db.schedule_attendance_fact (
  stat_date Date,
  movie_id UInt64,
  cinema_id UInt64,
  hall_id UInt64,
  schedule_id UInt64,
  seat_count UInt32,
  sold_seat_count UInt32,
  attendance_rate Float32
)
ENGINE = Log;

CREATE TABLE movie_clickhouse_db.city_boxoffice_stat (
  stat_date Date,
  region_code String,
  movie_id UInt64,
  ticket_order_count UInt64,
  payment_amount Decimal(18,2)
)
ENGINE = Log;

CREATE TABLE movie_clickhouse_db.movie_rating_distribution (
  stat_date Date,
  movie_id UInt64,
  rating UInt8,
  rating_count UInt64
)
ENGINE = Log;

CREATE TABLE movie_clickhouse_db.movie_popularity_trend (
  stat_date Date,
  movie_id UInt64,
  comment_count UInt64,
  rating_count UInt64,
  search_count UInt64,
  hot_score Float64
)
ENGINE = Log;

CREATE TABLE movie_clickhouse_db.recommend_exposure_click_fact (
  stat_date Date,
  user_md5 String,
  movie_id UInt64,
  exposure_count UInt64,
  click_count UInt64,
  conversion_rate Float64
)
ENGINE = Log;

CREATE TABLE movie_clickhouse_db.search_heat_stat (
  stat_date Date,
  keyword String,
  movie_id UInt64,
  search_count UInt64
)
ENGINE = Log;

CREATE TABLE movie_clickhouse_db.movie_hot_ranking_snapshot (
  snapshot_time DateTime,
  stat_date Date,
  rank_type String,
  rank_no UInt16,
  movie_id UInt64,
  hot_score Float64,
  source String
)
ENGINE = Log;

TRUNCATE TABLE IF EXISTS movie_clickhouse_db.boxoffice_fact;
TRUNCATE TABLE IF EXISTS movie_clickhouse_db.schedule_attendance_fact;
TRUNCATE TABLE IF EXISTS movie_clickhouse_db.city_boxoffice_stat;
TRUNCATE TABLE IF EXISTS movie_clickhouse_db.movie_rating_distribution;
