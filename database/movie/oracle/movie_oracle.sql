CREATE TABLE PUBLISHER_COMPANY (
  PUBLISHER_COMPANY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NAME VARCHAR2(200) NOT NULL,
  REGION_CODE VARCHAR2(32),
  COMPANY_STATUS VARCHAR2(16) DEFAULT 'ACTIVE',
  CREATED_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE PRODUCTION_COMPANY (
  PRODUCTION_COMPANY_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NAME VARCHAR2(200) NOT NULL,
  REGION_CODE VARCHAR2(32),
  COMPANY_STATUS VARCHAR2(16) DEFAULT 'ACTIVE',
  CREATED_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE MOVIE_COPYRIGHT (
  COPYRIGHT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MOVIE_ID NUMBER NOT NULL,
  COPYRIGHT_STATUS VARCHAR2(16) NOT NULL,
  RIGHT_TYPE VARCHAR2(32),
  LICENSE_START_DATE DATE,
  LICENSE_END_DATE DATE,
  CREATED_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  CONSTRAINT UK_MOVIE_COPYRIGHT_MOVIE UNIQUE (MOVIE_ID)
);

CREATE TABLE LICENSE_CONTRACT (
  CONTRACT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MOVIE_ID NUMBER NOT NULL,
  PUBLISHER_COMPANY_ID NUMBER NOT NULL,
  PRODUCTION_COMPANY_ID NUMBER NOT NULL,
  CONTRACT_NO VARCHAR2(64) NOT NULL,
  SIGN_DATE DATE,
  EFFECTIVE_DATE DATE,
  EXPIRE_DATE DATE,
  CONTRACT_STATUS VARCHAR2(16) NOT NULL,
  CONTRACT_AMOUNT NUMBER(14,2),
  CONSTRAINT UK_LICENSE_CONTRACT_NO UNIQUE (CONTRACT_NO),
  CONSTRAINT FK_LC_PUBLISHER FOREIGN KEY (PUBLISHER_COMPANY_ID) REFERENCES PUBLISHER_COMPANY(PUBLISHER_COMPANY_ID),
  CONSTRAINT FK_LC_PRODUCTION FOREIGN KEY (PRODUCTION_COMPANY_ID) REFERENCES PRODUCTION_COMPANY(PRODUCTION_COMPANY_ID)
);

CREATE TABLE RELEASE_REGION_AUTH (
  AUTH_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MOVIE_ID NUMBER NOT NULL,
  CONTRACT_ID NUMBER NOT NULL,
  REGION_CODE VARCHAR2(32) NOT NULL,
  RELEASE_DATE DATE,
  AUTH_STATUS VARCHAR2(16) NOT NULL,
  APPROVAL_TIME TIMESTAMP,
  CONSTRAINT FK_RRA_CONTRACT FOREIGN KEY (CONTRACT_ID) REFERENCES LICENSE_CONTRACT(CONTRACT_ID)
);

CREATE TABLE SCHEDULE_APPROVAL (
  APPROVAL_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MOVIE_ID NUMBER NOT NULL,
  APPROVAL_STATUS VARCHAR2(16) NOT NULL,
  APPROVAL_TIME TIMESTAMP,
  APPROVER_NAME VARCHAR2(64),
  REMARK VARCHAR2(500)
);

CREATE TABLE BOXOFFICE_SETTLEMENT_RULE (
  RULE_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  MOVIE_ID NUMBER NOT NULL,
  PUBLISHER_COMPANY_ID NUMBER NOT NULL,
  PRODUCTION_COMPANY_ID NUMBER NOT NULL,
  PUBLISHER_RATIO NUMBER(5,2) NOT NULL,
  PRODUCTION_RATIO NUMBER(5,2) NOT NULL,
  EFFECTIVE_DATE DATE NOT NULL,
  EXPIRE_DATE DATE,
  RULE_STATUS VARCHAR2(16) DEFAULT 'ACTIVE',
  CONSTRAINT FK_BSR_PUBLISHER FOREIGN KEY (PUBLISHER_COMPANY_ID) REFERENCES PUBLISHER_COMPANY(PUBLISHER_COMPANY_ID),
  CONSTRAINT FK_BSR_PRODUCTION FOREIGN KEY (PRODUCTION_COMPANY_ID) REFERENCES PRODUCTION_COMPANY(PRODUCTION_COMPANY_ID)
);

CREATE TABLE PARTNER_SETTLEMENT_RECORD (
  SETTLEMENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  RULE_ID NUMBER NOT NULL,
  MOVIE_ID NUMBER NOT NULL,
  PARTNER_COMPANY_ID NUMBER,
  SETTLEMENT_AMOUNT NUMBER(14,2) NOT NULL,
  SETTLEMENT_TIME TIMESTAMP,
  SETTLEMENT_STATUS VARCHAR2(16) NOT NULL,
  PERIOD_MONTH VARCHAR2(7),
  CONSTRAINT FK_PSR_RULE FOREIGN KEY (RULE_ID) REFERENCES BOXOFFICE_SETTLEMENT_RULE(RULE_ID),
  CONSTRAINT UK_PSR_MOVIE_PERIOD UNIQUE (MOVIE_ID, PERIOD_MONTH)
);

CREATE INDEX IDX_LC_MOVIE_ID ON LICENSE_CONTRACT(MOVIE_ID);
CREATE INDEX IDX_RRA_MOVIE_ID ON RELEASE_REGION_AUTH(MOVIE_ID);
CREATE INDEX IDX_SA_MOVIE_ID ON SCHEDULE_APPROVAL(MOVIE_ID);
CREATE INDEX IDX_PSR_MOVIE_TIME ON PARTNER_SETTLEMENT_RECORD(MOVIE_ID, SETTLEMENT_TIME);

